package com.class14;

public class Practice {
	
	String onePiece(String character) {
		if (character.equals("Luffy")) {
			return "I am going to be king of the Pirates!";
		}else if (character.equals("Zoro")) {
			return "Worlds greatest swordsman";
		}else if (character.equals("Sanji")) {
			return "Nami Swannnnnnnn";
		}else {
			return "Over Luffy?";
		}
	}
	
	

	int year(int shoes) {
		if(shoes==1) {
			return 85;
		}else if (shoes==2) {
			return 87;
		}else if (shoes == 3) {
			return 88;
		}else {
			return 0;
		}
	}
	
	

}
